  }
})
const handleAlignChange = val => {
  console.log("align.value", val)
  table.value.operatorConfig.align = val
}
// 设置日期左对齐
const setDateLeft = () => {
  table.value.columns.forEach(item => {
    if (item.prop === "date") {
      if (item.align == "left") {
        item.align = ""
        setDate.value = "设置日期"
      } else {
        setDate.value = "取消日期"
        item.align = "left"
      }
    }
  })
}
// 设置姓名右对齐
const setNameRight = () => {
  table.value.columns.forEach(item => {
    if (item.prop === "name") {
      if (item.align == "right") {
        item.align = ""
        setName.value = "设置姓名"
      } else {
        setName.value = "取消姓名"
        item.align = "right"
      }
    }
  })
}
